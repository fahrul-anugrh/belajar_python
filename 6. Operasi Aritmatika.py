# Operasi Aritmatika

## Operasi Penjumlahan
a = 10
b = 2
hasil = a + b
print (a, "+", b, "=", hasil)

## Operasi Pengurangan
hasil = a - b
print (a, "-", b, "=", hasil)

## Operasi Perkalian
hasil = a * b
print (a, "x", b, "=", hasil)

## Operasi Pembagian
hasil = a / b
print (a, "/", b, "=", hasil)

## Operasi Eksponen (pangkat)
hasil = a ** b
print (a, "^", b, "=", hasil)

## Operasi Modulus (sisa bagi)
hasil = a % b
print (a, "mod", b, "=", hasil)

## Operasi Floor divison (kebalikan modulus : pembagian yang berhasil/pembulatan kebawah)
hasil = a // b
print (a, "//", b, "=", hasil)

